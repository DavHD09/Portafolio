function abrirmenu() {
    document.getElementById("filaultima").style.display = "block"
    document.getElementById("ampliar").style.display = "none";
    document.getElementById("cerrar").style.display = "inline"
}

function cerrarmenu() {
    document.getElementById("filaultima").style.display = "none"
    document.getElementById("ampliar").style.display = "inline";
    document.getElementById("cerrar").style.display = "none"
}